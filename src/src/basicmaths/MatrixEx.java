package basicmaths;

import java.util.*;

public class MatrixEx {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for(int i=n;i<1;i--){
            int temp = 0;
            temp = temp + n;
            if(i==1) System.out.println("\n");
        }
        sc.close();
    }
}